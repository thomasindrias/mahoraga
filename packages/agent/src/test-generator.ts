import type { IssueGroup } from '@mahoraga/core';

/**
 * Result of generating a localized test.
 */
export interface GeneratedTest {
  /** File path where the test was written */
  testPath: string;
  /** Test file content */
  content: string;
  /** Command to run the test */
  runCommand: string;
}

/**
 * Generate a localized test that mimics the user journey which triggered the issue.
 * The test verifies the fix addresses the root cause.
 * @param issue - The detected issue
 * @param workDir - Working directory of the project
 * @returns Generated test details
 */
export function generateTest(
  issue: IssueGroup,
  workDir: string,
): GeneratedTest {
  const testFileName = `mahoraga-verify-${issue.ruleId}-${sanitizeForFilename(issue.id)}.test.ts`;
  const testPath = `${workDir}/__mahoraga_tests__/${testFileName}`;

  let content: string;

  switch (issue.ruleId) {
    case 'rage-clicks':
      content = generateRageClickTest(issue);
      break;
    case 'error-spikes':
      content = generateErrorSpikeTest(issue);
      break;
    default:
      content = generateGenericTest(issue);
      break;
  }

  return {
    testPath,
    content,
    runCommand: `npx vitest run ${testPath}`,
  };
}

