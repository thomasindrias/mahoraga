function sanitizeForFilename(str: string): string {
  return str.replace(/[^a-zA-Z0-9-]/g, '-').substring(0, 50);
}

function escapeString(str: string): string {
  return str.replace(/'/g, "\\'").replace(/\n/g, ' ');
}
